	<div class="bearingsForReading">Czytanie: 1 Kor 1, 26-29</div>
	<div class="titleReading">Prawdziwa mądrość zapewni jedność</div>

	<p><sub>26</sub>Przeto przypatrzcie się, bracia, powołaniu waszemu! Niewielu tam mędrców według oceny ludzkiej, niewielu możnych, niewielu szlachetnie urodzonych. <sub>27</sub> Bóg wybrał właśnie to, co głupie w oczach świata, aby zawstydzić mędrców, wybrał to, co niemocne, aby mocnych poniżyć; <sub>28</sub> i to, co nie jest szlachetnie urodzone według świata i wzgardzone, i to, co nie jest, wyróżnił Bóg, by to co jest, unicestwić, <sub>29</sub> tak by się żadne stworzenie nie chełpiło wobec Boga.</p>
