	<p><sub>24</sub> Ale Tomasz, jeden z Dwunastu, zwany Didymos, nie był razem z nimi, kiedy przyszedł Jezus. <sub>25</sub> Inni więc uczniowie mówili do niego: «Widzieliśmy Pana!» Ale on rzekł do nich: «Jeżeli na rękach Jego nie zobaczę śladu gwoździ i nie włożę palca mego w miejsce gwoździ, i nie włożę ręki mojej do boku Jego, nie uwierzę». <sub>26</sub> A po ośmiu dniach, kiedy uczniowie Jego byli znowu wewnątrz [domu] i Tomasz z nimi, Jezus przyszedł mimo drzwi zamkniętych, stanął pośrodku i rzekł: «Pokój wam!» <sub>27</sub> Następnie rzekł do Tomasza: «Podnieś tutaj swój palec i zobacz moje ręce. Podnieś rękę i włóż [ją] do mego boku, i nie bądź niedowiarkiem, lecz wierzącym!» <sub>28</sub> Tomasz Mu odpowiedział: «Pan mój i Bóg mój!» <sub>29</sub> Powiedział mu Jezus: «Uwierzyłeś dlatego, ponieważ Mnie ujrzałeś? Błogosławieni, którzy nie widzieli, a uwierzyli».</p>
